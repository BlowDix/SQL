DROP TABLE IF EXISTS TEMPERATURA;

	CREATE TABLE TEMPERATURA(

	dia DATE NOT NULL,
	codigo_CIUDAD integer(5) NOT NULL,
	grados varchar(20) NOT NULL,
	PRIMARY KEY(dia),
	FOREIGN KEY (codigo_CIUDAD)
		REFERENCES CIUDAD(codigo)
		ON UPDATE CASCADE ON DELETE CASCADE
	
);

